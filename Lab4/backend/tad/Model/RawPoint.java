package Model;

public class RawPoint {
    private String x;
    private String y;
    private String r;

    public RawPoint(String x, String y, String r) {
        this. x = x;
        this.y = y;
        this.r = r;
    }

    public RawPoint(){

    }
    public String getX() {
        return x;
    }
    public String getY() {
        return y;
    }

    public String getR() {
        return r;
    }

}
